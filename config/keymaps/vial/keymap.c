// ============================================================
// RGB Matrix 情報表示 (Mode / Hue / Val)
// 3行 (各行5文字幅) を順に書き込む
// ============================================================
static void render_rgb_info(void) {
#ifdef RGB_MATRIX_ENABLE
    char buf[6];
    if (rgb_matrix_is_enabled()) {
        snprintf(buf, sizeof(buf), "M:%02d ", rgb_matrix_get_mode());
        oled_write(buf, false);
        snprintf(buf, sizeof(buf), "H:%03d", rgb_matrix_get_hue());
        oled_write(buf, false);
        snprintf(buf, sizeof(buf), "V:%03d", rgb_matrix_get_val());
        oled_write(buf, false);
    } else {
        oled_write("RGB  ", false);
        oled_write(" OFF ", false);
        oled_write("     ", false);
    }
#else
    oled_write("     ", false);
    oled_write("     ", false);
    oled_write("     ", false);
#endif
}

