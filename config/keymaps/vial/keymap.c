// 【変更1】 slave_data() を以下で完全置換
// 既存のものを消して、これに丸ごと差し替えてください
// ============================================================
static void slave_data(void) {
    render_logo();                                  // row 0-2: Trackball 39
    oled_set_cursor(0, 4);
    render_mod_status_gui_alt(get_mods());          // row 4-5
    render_mod_status_ctrl_shift(get_mods());       // row 6-7

    // Layer name (row 8)
    oled_set_cursor(0, 8);
    switch (get_highest_layer(layer_state)) {
        case LAYER_BASE:   oled_write("Base ", false); break;
        case LAYER_MOUSE:  oled_write("Mouse", false); break;
        case LAYER_SYMBOL: oled_write("Sym  ", false); break;
        case LAYER_RGB:    oled_write("RGB  ", false); break;
        case LAYER_NUMBER: oled_write("Num  ", false); break;
        case LAYER_NUMPAD: oled_write("Npad ", false); break;
        default:           oled_write("?????", false); break;
    }

    // RGB info (rows 9-11)
    oled_set_cursor(0, 9);
    render_rgb_info();

    // KKR ロゴ (rows 12-15, 32×32 アニメーション)
    render_kkr_logo(0, 12);
}


